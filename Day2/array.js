// Array Operation's
let arr = ["I", "Am", "Amogh"];
// Printing Array
console.log(arr);
// Output: - [ 'I', 'Am', 'Amogh' ]

// Adding element at the end of an array
arr.push("Gupta");
console.log(arr);
// Output: - [ 'I', 'Am', 'Amogh', "Gupta" ]

// Deleting element at the end of an array
arr.pop();
console.log(arr);
// Output: - [ 'I', 'Am', 'Amogh' ]

// Adding element at the starting of an array
arr.unshift("Hey");
console.log(arr);
// Output: - [ 'Hey', 'I', 'Am', 'Amogh' ]

// Deleting element at the starting of an array
arr.shift();
console.log(arr);
// Output: - [ 'I', 'Am', 'Amogh' ]